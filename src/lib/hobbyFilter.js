// Every exact spelling (clean word, censored variant, or whole-word-only
// term) that counts as a match when a typed name equals it exactly as one
// "word".
const WHOLE_WORD_LOOKUP = new Set(CURSE_WHOLE_WORD_ONLY);
CURSE_WORDS.forEach(([clean, variants]) => {
  WHOLE_WORD_LOOKUP.add(clean);
  variants.forEach((v) => WHOLE_WORD_LOOKUP.add(v));
});

function containsCurseWord(name) {
  const words = name.split(/[\s,，、.!?！？~～\-_/]+/).filter(Boolean);
  if (words.some((w) => WHOLE_WORD_LOOKUP.has(w))) return true;
  // Clean (uncensored) words can still show up glued to other text with no
  // separator ("fuckthis") — substring-match those directly. Never do this
  // for the censored variants or the whole-word-only list: substring-
  // matching "f*ck" or "ass" would defeat the point of requiring a whole
  // word and reintroduce false positives like "class" or "peacock".
  return CURSE_WORDS.some(([clean]) => name.includes(clean));
}

export function isBlockedHobby(name) {
  const n = String(name || "").trim().toLowerCase();
  if (!n) return false;
  if (BLOCKED_TERMS.some((term) => n.includes(term))) return true;
  return containsCurseWord(n);
}
