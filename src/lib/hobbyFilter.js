// Curse words and slurs. Each one lists the realistic single-character
// censorship spellings people actually type (an asterisk standing in for
// one letter — "f*ck", "sh*t"), hand-checked one at a time rather than
// generated by a live wildcard pattern. A generic wildcard match ("f*ck"
// matches anything shaped like f.ck) previously caught innocent words too —
// "*uck" matched "duck" and "luck" just as well as "fuck". Matching only
// this fixed, manually-verified list of exact spellings closes that gap:
// nothing gets blocked unless it is either a clean word from this list, or
// one of the specific censored spellings written out below.
//
// Short words (ass, dick, cock, piss, twat) are deliberately whole-word-only
// — never substring-matched — since they're real substrings of ordinary
// words ("class", "peacock", "predict").
